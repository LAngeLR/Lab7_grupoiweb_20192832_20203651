package com.example.lab7_20192832_20203651.ModelsLab.BeansLab;

public class Estadio {

    private int idEstadio;
    private String nombreEstadio;
    private String provinciaEstadio;
    private String clubEstadio;

    public int getIdEstadio() {
        return idEstadio;
    }

    public void setIdEstadio(int idEstadio) {
        this.idEstadio = idEstadio;
    }

    public String getNombreEstadio() {
        return nombreEstadio;
    }

    public void setNombreEstadio(String nombreEstadio) {
        this.nombreEstadio = nombreEstadio;
    }

    public String getProvinciaEstadio() {
        return provinciaEstadio;
    }

    public void setProvinciaEstadio(String provinciaEstadio) {
        this.provinciaEstadio = provinciaEstadio;
    }

    public String getClubEstadio() {
        return clubEstadio;
    }

    public void setClubEstadio(String clubEstadio) {
        this.clubEstadio = clubEstadio;
    }
}
