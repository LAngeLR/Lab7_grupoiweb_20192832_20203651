package com.example.lab7_20192832_20203651.ModelsLab.BeansLab;

public class Arbitro {

    private int idArbitro;

    private String nombreArbitro;

    private String paisArbitro;

    public int getIdArbitro() {
        return idArbitro;
    }

    public void setIdArbitro(int idArbitro) {
        this.idArbitro = idArbitro;
    }

    public String getNombreArbitro() {
        return nombreArbitro;
    }

    public void setNombreArbitro(String nombreArbitro) {
        this.nombreArbitro = nombreArbitro;
    }

    public String getPaisArbitro() {
        return paisArbitro;
    }

    public void setPaisArbitro(String paisArbitro) {
        this.paisArbitro = paisArbitro;
    }
}
